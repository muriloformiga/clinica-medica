
package br.ufs.model;


public class TipoPatrimonio {
    private boolean tangivel;
    private boolean intangivel;
    private boolean movel;
    private boolean imovel;

    public boolean isTangivel() {
        return tangivel;
    }

    public void setTangivel(boolean tangivel) {
        this.tangivel = tangivel;
    }

    public boolean isIntangivel() {
        return intangivel;
    }

    public void setIntangivel(boolean intangivel) {
        this.intangivel = intangivel;
    }

    public boolean isMovel() {
        return movel;
    }

    public void setMovel(boolean movel) {
        this.movel = movel;
    }

    public boolean isImovel() {
        return imovel;
    }

    public void setImovel(boolean imovel) {
        this.imovel = imovel;
    }
    
    
}
